package com.java.basics;

public class Comments {
    public static void main(String[] args) {
        // Single-line comment before a line of code
        System.out.println("Comentario Simple sobre la Línea de Código");
        System.out.println("Comentario Simple al final de la Línea de Código"); // Single-line comment at the end of a line of code
        /* Multi-line
        Comment*/
        System.out.println("Comentario Multi Línea");
    }
}
